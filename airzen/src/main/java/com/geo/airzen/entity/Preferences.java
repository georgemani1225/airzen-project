package com.geo.airzen.entity;

public class Preferences {

	private String digitalAd;
	private String seatPref;
	private String shippingAddr;
	private String mealPref;
	private String pajamaSize;
	private String dayPriorContact;

	public String getDigitalAd() {
		return digitalAd;
	}

	public String getSeatPref() {
		return seatPref;
	}

	public String getShippingAddr() {
		return shippingAddr;
	}

	public String getMealPref() {
		return mealPref;
	}

	public String getPajamaSize() {
		return pajamaSize;
	}

	public String getDayPriorContact() {
		return dayPriorContact;
	}

	public void setDigitalAd(String digitalAd) {
		this.digitalAd = digitalAd;
	}

	public void setSeatPref(String seatPref) {
		this.seatPref = seatPref;
	}

	public void setShippingAddr(String shippingAddr) {
		this.shippingAddr = shippingAddr;
	}

	public void setMealPref(String mealPref) {
		this.mealPref = mealPref;
	}

	public void setPajamaSize(String pajamaSize) {
		this.pajamaSize = pajamaSize;
	}

	public void setDayPriorContact(String dayPriorContact) {
		this.dayPriorContact = dayPriorContact;
	}

}
